
public class Category {
	int categoryID;
	String categoryName;
	int amountOfCourses;
	
	
	public Category() {
		
	}
	public Category(int categoryID,String categoryName,int amountOfCourses) {
		this.categoryID = categoryID;
		this.categoryName = categoryName;
		this.amountOfCourses = amountOfCourses;
	}
}
